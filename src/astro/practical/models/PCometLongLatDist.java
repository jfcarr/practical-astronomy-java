package astro.practical.models;

public class PCometLongLatDist {
    public double cometLongDeg;
    public double cometLatDeg;
    public double cometDistAU;

    public PCometLongLatDist(double cometLongDeg, double cometLatDeg, double cometDistAU) {
        this.cometLongDeg = cometLongDeg;
        this.cometLatDeg = cometLatDeg;
        this.cometDistAU = cometDistAU;
    }
}
